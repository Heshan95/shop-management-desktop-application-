    public void searchModel() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) searchTable.getModel();
            dtm.setRowCount(0);

            ResultSet rs = DB.dbConnection.getConnection().createStatement().executeQuery("SELECT * FROM products WHERE product_barcode LIKE '%" + searchProducts.getText() + "%'");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("product_barcode"));
                v.add(rs.getString("category"));
                v.add(rs.getString("name"));
                v.add(rs.getString("selling_price"));
                v.add(rs.getString("qty"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
            log1.debug(e.getMessage());
        }
    }
    
    
    
