    void loadTabel(){
         try {
            DefaultTableModel dtm = (DefaultTableModel) businessHistoryTable.getModel();
            dtm.setRowCount(0);
            
            ResultSet rs = DB.dbConnection.getConnection().createStatement().executeQuery("SELECT * FROM invoice_item");
            while (rs.next()) {                
                Vector v= new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("invoice_id_invoice"));                
                v.add(rs.getString("name"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("netTotal"));
                v.add(rs.getString("discount"));
                
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
            log1.debug(e.getMessage());
        } 
    }
