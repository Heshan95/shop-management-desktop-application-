    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String barcode = barcodeIDField.getText();
        String first_name = fname.getText();
        String last_name = lname.getText();
        String mail = email.getText();
        String id = nic.getText();
        String phone = mobile.getText();
        String a1 = address1.getText();
        String a2 = address2.getText();
        String c = city.getText();
        String note = textArea.getText();
        if (lname.getText().equals("") && email.getText().equals("")&& nic.getText().equals("")&& mobile.getText().equals("")&&  address1.getText().equals("")&& address2.getText().equals("")&& city.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Fill Out The Customer Details");
            return;
        }
        if (!email.getText().contains("@") && !email.getText().contains(".")) {
                JOptionPane.showMessageDialog(this, "Invalid Email Address");
                return;
            }
        
        else if(mobile.getText().contains("abcdefghijklmnopqrstuvwxyz")){
        JOptionPane.showMessageDialog(this, "Please Fill Out The Corect Mobile Detail");
        return;
        }else{

        String resrultStaff = um.insertCustomer(barcode, first_name, last_name, mail, id, phone, a1, a2, c, note);

        JOptionPane.showConfirmDialog(rootPane, resrultStaff, "Customer Add", 0, 1);
        GUI.Customer.customerFrame.customerTableLoad();
        }
        clean();
    }//GEN-LAST:event_addBtnActionPerformed

