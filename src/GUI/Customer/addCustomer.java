    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String barcode = barcodeIDField.getText();
        if (barcode.equals("")) {
            JOptionPane.showConfirmDialog(rootPane, "Please Fill Out The Barcode", "", -1, 1);
        } else {
            String result = um.selectCustomer(barcode);
            if (result.equals("have")) {
                JOptionPane.showConfirmDialog(rootPane, "Customer Already Registered", "", -1, 2);
                barcodeIDField.setText("");
            } else {
                JOptionPane.showConfirmDialog(rootPane, "New Customers ID", "", -1, 1);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

