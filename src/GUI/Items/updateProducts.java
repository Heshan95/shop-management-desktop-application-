    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String barcode = bacordField.getText();
        if (bacordField.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill Barcode of Product");
                return;
            }
        String category = jComboBox1.getSelectedItem().toString() ;
        String n = name.getText();
        String tArea = textArea.getText();
       // String u = uPrice.getText();
        double u = Double.parseDouble(uPrice.getText());
         if (uPrice.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill unit price");
                return;
            }
        String q = qty.getText();
         if (qty.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill amount of qty");
                return;
            }
        

        String result = um.updateItem(barcode, category, n, tArea, u, q);
        um.loadTable("", 0, userTableModel);
         GUI.Items.itemsFrame.loadProductTable(); 
        JOptionPane.showConfirmDialog(rootPane, result, "Message !", 0, 1); 
        clear();
    }//GEN-LAST:event_jButton1ActionPerformed

